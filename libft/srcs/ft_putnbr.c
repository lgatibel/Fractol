#include "libft.h"

void		ft_putnbr(int n)
{
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			if (n < -9 || n > -1)
				ft_putnbr(-n / 10);
			n = -n;
		}
		else if (n > 9)
		{
			ft_putnbr(n / 10);
		}
		ft_putchar(n % 10 + 48);
	}
}
