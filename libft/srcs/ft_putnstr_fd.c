#include "libft.h"

void	ft_putnstr_fd(char const *s, int fd, int n)
{
	int i;

	i = 0;
	if (s)
		while (s[i] && i < n)
		{
			ft_putchar_fd(s[i], fd);
			i++;
		}
}
