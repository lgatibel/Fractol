#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i1;
	int res;

	i1 = 0;
	res = 0;
	if (s1 && s2)
	{
		while ((s1[i1]) && (s2[i1]))
		{
			if (s1[i1] == s2[i1])
				res++;
			i1++;
		}
		if (i1 == res && (!s1[i1]) && (!s2[i1]))
			return (1);
	}
	return (0);
}
